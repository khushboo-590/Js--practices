// Data types in JavaScript define the data type that a variable can store.
// JavaScript includes primitive and non - primitive data types



// JavaScript includes primitive

// 1.strings,
// 2.numbers,
// 3.booleans,
// 4.undefined,
// 5.null

// --------string----

// A string(or a text string) is a series of characters.

// Strings are written with quotes.You can use single or double, Backticks quotes:



let fruit = 'apple';
console.log(fruit)

let country = "USA";
console.log(country);

let result = `fail`;
console.log(result);



// ---------number-------


// In JavaScript the number type represents numeric values(both integers and floating - point numbers).



let integer = -3;
console.log(integer);

let float_number = 3.15;
console.log(float_number);

// -------Boolean-----


// In JavaScript, a Boolean is a data type that represents true or false.It is commonly used in conditional statements and logical operations.


let Student = true;
let Teacher = false;

console.log(Student); 
console.log(Teacher); 

// ------null------

// In JavaScript, null represents "no value" or "nothing."



let number = null;
console.log(number);


// --------undefined------



// If a variable is declared but the value is not assigned, then the value of that variable will be undefined.


let name;
console.log(name);  


// ---------Bigint------

let bigNumber = 123456789012345678901234567890
